main() {
  // Operadores Aritiméticos (operadores binario/infix)
  int a = 7;
  int b = 3;

  int resultado = a + b;
  print(resultado);
  print(a - b);
  print(a * b);
  print(a / b);
  print(33 % 2);
  print(34 % 2);
  print(a + b * a - b);

  // Operadores lógicos
  bool c = true;
  bool d = false;

  print(c && d); // E
  print(c || d); // OU
  print(c ^ d); // OU Exclusivo
  print(!c); // NÃO
  print(!!c); //NÃO NÃO KKKKK
}
